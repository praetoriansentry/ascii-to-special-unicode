func remap(input string, table []rune) string {
	output := ""
	for k := 0; k < len(input); k = k + 1 {
		myRune := input[k]
		newKey := int(myRune) - 33
		if newKey < 0 {
			output += " "
			continue
		}
		output += string(table[newKey])
	}
	return output
}

func isASCII(testString string) bool {
	for k := 0; k < len(testString); k = k + 1 {
		if testString[k] >  0x7e {
			return false
		}
		if testString[k] < 0x20 {
			return false
		}
	}
	return true
}
